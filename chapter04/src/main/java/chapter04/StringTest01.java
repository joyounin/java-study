package chapter04;

public class StringTest01 {

	public static void main(String[] args) {
		// c:\temp
		System.out.println("c:\\temp");
		
		// "HELLO"
		System.out.println("\"HELLO\"");
		// \t : tab
		// \r : carriage return
		// \n : new line
		// \b : bell
		System.out.println("hello\twrold\n");
		System.out.println("hello\twrold");
		
		// '
		char c = '\'';
		System.out.println(c);

	}

}
